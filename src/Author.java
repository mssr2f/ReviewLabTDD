


public class Author {

	private String firstName;
	private String lastName;
	private String city;
	//constructor
	
	

	public Author( String first, String last, String city){
		
		this.firstName = first;
		this.lastName = last;
		this.city = city;
	}

	public String getFirstName() {
		return firstName;
	}

	public String getLastName() {
		return lastName;
	}
}
